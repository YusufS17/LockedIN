// MARK: - PixelIconView

/// Displays a PixelIcon baked at 1px/cell, nearest-neighbour scaled to `size`.
struct PixelIconView: View {
    let icon: PixelIcon
    var size: CGFloat = 18
    var tint: Color = Theme.Colour.textPrimary

    var body: some View {
        let rgba = PixelRGBA(tint)
        Image(uiImage: SpriteBakery.shared.image(key: "px-icon|\(icon.rawValue)|\(rgba.keyFragment)") {
            PixelRenderer.bake(
                grid: icon.grid,
                palette: PixelPalette([
                    "#": rgba,
                    "+": rgba.lightened(0.30),
                    "o": PixelRGBA(Theme.Colour.surface),
                ]),
                rules: .flatIcon,
                outline: nil
            )
        })
        .interpolation(.none)
        .resizable()
        .scaledToFit()
        .frame(width: size, height: size)
        .accessibilityHidden(true)
    }
}

extension PixelMaterialRules.Rules {
    /// Flat-fill icon rules: no shading, no groups — glyph colours are literal.
    static let flatIcon = PixelMaterialRules.Rules(
        isEmpty: { $0 == "." || $0 == " " },
        isFlat: { _ in true },
        group: { _ in 0 }
    )
}

